       public void popTable(){
        DefaultTableModel model = (DefaultTableModel) adSpacejTable.getModel();
        model.setRowCount(0);
        for (AdSpace adSpace : adPublisehrenterprise.getAdSpaceList().getAdSpaceList()){
            Object row[] = new Object[5];
                row[0] = adSpace;
                row[1] = adSpace.getBasePrice();
                row[2] = adSpace.getSizeType();
                row[3] = adSpace.getLocationType();
                row[4] = adSpace.getProduct();
                
                ((DefaultTableModel) adSpacejTable.getModel()).addRow(row);
     }
       }
    
    
     public void populateCombOX(){
     sizejComboBox.removeAllItems();
     for (AdSpace.SizeType stype : AdSpace.SizeType.values()){
         sizejComboBox.addItem(stype);
     } 
     locationjComboBox.removeAllItems();
     for(AdSpace.LocationType ltype: AdSpace.LocationType.values()){
         locationjComboBox.addItem(ltype);
     } 
    }
  
