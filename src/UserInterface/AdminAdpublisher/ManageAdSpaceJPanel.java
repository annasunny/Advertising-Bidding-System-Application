        String basePriceS = basePricejTextField1.getText();
        Double basePrice = Double.parseDouble(basePriceS);
        
        AdSpace.LocationType ltype = (AdSpace.LocationType) locationjComboBox.getSelectedItem();
        AdSpace.SizeType stype = (AdSpace.SizeType) sizejComboBox.getSelectedItem();
